"""
worldRefactored.py — worldcode.py's generation primitives, ported to a hosted
`llama-server` (llama.cpp tools/server) using GBNF grammars + JSON-schema instead
of in-process logit surgery.

Design shift from worldcode.py
------------------------------
  * No in-process model, no KV manipulation, no rollback. Talk HTTP to llama-server.
  * Constrained generation via `grammar` (GBNF) / `json_schema`, NOT manual logit
    masking + token tries. This declaratively replaces promptNoun's stop-lists,
    parseNumber's number-token trie, getLocations2's huge stop set, etc.
  * `prsOfStrings` (full distribution over a candidate set via KV rollback) is
    DROPPED — it was a small-model hack. With a larger base model we just *pick*
    from the set with an alternation grammar (`pick_from_set`).
  * Probabilities (the old yesVsNo) come from `n_probs` on the first token.
  * Base-model friendly: raw continuation prompts, no chat template — which also
    deletes the old `"if was real"` fiction-puncturing fight (no RLHF to break char).
  * `cache_prompt=True` gives prefix reuse for free (replaces the manual eval/reset
    overlap logic in Model.eval).

Run a server first, e.g.:
  llama-server -m GLM-4-32B-Base.gguf -c 8192 --host 127.0.0.1 --port 8080

NOTE: the `n_probs` response schema has varied across llama.cpp versions
(`completion_probabilities` vs `logprobs`, `prob` vs `logprob`); `_first_token_probs`
parses defensively but check it against your build if `yes_no_prob` looks off.
"""

